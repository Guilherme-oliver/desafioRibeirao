package interruptores;

public class Interruptores {
    public static void main(String[] args) {
        System.out.println("1. Ligue o primeiro interruptor e deixe ligado por alguns minutos.");
        System.out.println("2. Desligue o primeiro interruptor e ligue o segundo.");
        System.out.println("3. Vá até a sala das lâmpadas:");
        System.out.println("   - A lâmpada acesa corresponde ao segundo interruptor.");
        System.out.println("   - A lâmpada quente corresponde ao primeiro interruptor.");
        System.out.println("   - A lâmpada fria corresponde ao terceiro interruptor.");
    }
}